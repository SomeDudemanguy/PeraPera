        JPanel comicListPanel = new JPanel();
        comicListPanel.setLayout(new BoxLayout(comicListPanel, BoxLayout.Y_AXIS));
        comicListPanel.setBackground(ApplicationService.getBackgroundMedium());
        
        // Create comic rows
        List<JPanel> comicRows = new ArrayList<>();
        for (int i = 0; i < collection.comicNames.size(); i++) {
            String comicName = collection.comicNames.get(i);
            JPanel row = createComicRow(comicName, i, collection, collectionEntry, libraryService, 
                                        lastReadComicId, dialog, comicRows);
            comicRows.add(row);
            comicListPanel.add(row);
        }
        
        if (collection.comicNames.isEmpty()) {
            JLabel emptyLabel = new JLabel("No comics in collection", SwingConstants.CENTER);
            emptyLabel.setForeground(ApplicationService.getTextSecondary());
            emptyLabel.setFont(new Font("Arial", Font.ITALIC, 12));
            comicListPanel.add(emptyLabel);
        }
        
