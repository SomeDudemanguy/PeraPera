        headerPanel.setBackground(ApplicationService.getBackgroundMedium());
        headerPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        // Title
        String icon = collection.isAutoGenerated ? "[Series]" : "[Coll]";
        JLabel titleLabel = new JLabel(icon + " " + collection.name, SwingConstants.CENTER);
        titleLabel.setForeground(ApplicationService.getAccentColor());
        titleLabel.setFont(new Font("Arial", Font.BOLD, 14));
        headerPanel.add(titleLabel, BorderLayout.NORTH);
        
        // Info panel
        String info = collection.rating + "/100";
        if (!collection.tags.isEmpty()) {
            info += " | Tags: " + String.join(", ", collection.tags);
        }
        JLabel infoLabel = new JLabel(info, SwingConstants.CENTER);
        infoLabel.setForeground(ApplicationService.getTextPrimary());
        infoLabel.setFont(new Font("Arial", Font.ITALIC, 10));
        headerPanel.add(infoLabel, BorderLayout.CENTER);
        
        // Button panel for Mark All / Clear All
        JPanel buttonPanel = new JPanel(new FlowLayout(FlowLayout.CENTER, 10, 5));
        buttonPanel.setBackground(ApplicationService.getBackgroundMedium());
        
        JButton markAllBtn = new JButton("Mark All Read");
        markAllBtn.setForeground(ApplicationService.getTextPrimary());
        markAllBtn.setBackground(ApplicationService.getInputBackground());
        markAllBtn.setBorder(BorderFactory.createLineBorder(ApplicationService.getAccentColor(), 1));
        markAllBtn.setFont(new Font("Arial", Font.PLAIN, 11));
        markAllBtn.addActionListener(e -> {
            libraryService.markAllAsRead(collection.name);
            refreshComicList(mainPanel, collection, libraryService, collectionEntry, lastReadComicId, dialog);
        });
        
        JButton clearAllBtn = new JButton("Clear Progress");
        clearAllBtn.setForeground(ApplicationService.getTextPrimary());
        clearAllBtn.setBackground(ApplicationService.getInputBackground());
        clearAllBtn.setBorder(BorderFactory.createLineBorder(ApplicationService.getAccentColor(), 1));
        clearAllBtn.setFont(new Font("Arial", Font.PLAIN, 11));
        clearAllBtn.addActionListener(e -> {
            libraryService.clearAllProgress(collection.name);
            lastReadIdRef[0] = null;
            refreshComicList(mainPanel, collection, libraryService, collectionEntry, null, dialog);
        });
        
        buttonPanel.add(markAllBtn);
        buttonPanel.add(clearAllBtn);
        headerPanel.add(buttonPanel, BorderLayout.SOUTH);
        
        mainPanel.add(headerPanel, BorderLayout.NORTH);
        
        // Read Collection button
        boolean hasProgress = lastReadComicId != null && !lastReadComicId.isEmpty();
        String buttonText = hasProgress ? "> Resume Reading" : "> Read Collection";
        JButton readCollectionBtn = new JButton(buttonText);
        readCollectionBtn.setForeground(ApplicationService.getAccentColor());
        readCollectionBtn.setBackground(ApplicationService.getInputBackground());
        readCollectionBtn.setBorder(BorderFactory.createLineBorder(ApplicationService.getAccentColor(), 1));
        readCollectionBtn.setFont(new Font("Arial", Font.BOLD, 12));
        readCollectionBtn.setMaximumSize(new Dimension(Integer.MAX_VALUE, 30));
        readCollectionBtn.addActionListener(e -> {
            dialog.dispose();
            // Build ordered list of folders from collection using comic IDs (not names)
            List<File> orderedFolders = new ArrayList<>();
            for (String comicId : collectionEntry.comicIds) {
                ComicEntry comicEntry = libraryService.getLibraryCache().get(comicId);
                if (comicEntry != null && comicEntry.folderPath != null) {
                    File folder = new File(comicEntry.folderPath);
                    if (folder.exists()) {
                        orderedFolders.add(folder);
                    }
                }
            }
            
            if (orderedFolders.isEmpty()) {
                JOptionPane.showMessageDialog(app,
                    "No comics from this collection were found in your library.",
                    "Collection Empty", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Smart Resume: If lastReadComicId exists, find and open that comic
            File comicToOpen = null;
            if (hasProgress && lastReadIdRef[0] != null) {
                ComicEntry lastReadEntry = libraryService.getLibraryCache().get(lastReadIdRef[0]);
                if (lastReadEntry != null && lastReadEntry.folderPath != null) {
                    File lastReadFolder = new File(lastReadEntry.folderPath);
                    if (lastReadFolder.exists()) {
                        comicToOpen = lastReadFolder;
                    }
                }
            }
            
            // If no progress found or comic not found, start from first
            if (comicToOpen == null) {
                comicToOpen = orderedFolders.get(0);
            }
            
            // IMMEDIATELY update lastReadComicId when opening via Read Collection button
            String openComicId = libraryService.getComicId(comicToOpen);
            if (openComicId != null) {
                libraryService.updateLastReadComic(collection.name, openComicId);
                System.out.println("DEBUG ReadCollection: Updated lastRead to " + openComicId);
            }
            
            app.openCollectionViewer(comicToOpen, orderedFolders, collection.name);
        });
        
