    /**
     * Escapes comic names for storage in collections (handles backslashes and commas).
     */
    public String escapeComicName(String name) {
        return name.replace("\\", "\\\\").replace(",", "\\,");
    }
    
    /**
     * Unescapes comic names from storage format.
     */
    public String unescapeComicName(String escaped) {
        return escaped.replace("\\,", ",").replace("\\\\", "\\");
    }
    
    /**
     * Joins comic names using escape-aware formatting.
     */
    public String joinComicNames(List<String> comicNames) {
        StringBuilder result = new StringBuilder();
        boolean first = true;
        
        for (String name : comicNames) {
            if (!first) result.append(",");
            first = false;
            result.append(escapeComicName(name));
        }
        
        return result.toString();
    }
    
    /**
     * Splits comic names using escape-aware parsing.
     */
    public List<String> splitComicNames(String comicsString) {
        List<String> result = new ArrayList<>();
        StringBuilder current = new StringBuilder();
        boolean escape = false;
        
        for (int i = 0; i < comicsString.length(); i++) {
            char c = comicsString.charAt(i);
            
            if (escape) {
                current.append(c);
                escape = false;
            } else if (c == '\\') {
                escape = true;
            } else if (c == ',') {
                result.add(unescapeComicName(current.toString()));
                current.setLength(0);
            } else {
                current.append(c);
            }
        }
        
        // Add the last name
        if (current.length() > 0) {
            result.add(unescapeComicName(current.toString()));
        }
        
        return result;
    }
    
