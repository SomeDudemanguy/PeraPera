    /**
     * Shows context menu for collection.
     */
    public void showCollectionContextMenu(Collection collection, JPanel source, MouseEvent e) {
        JPopupMenu popup = new JPopupMenu();
        
        JMenuItem editItem = new JMenuItem("⚙️ Edit Collection");
        editItem.addActionListener(ev -> DialogService.showEditCollectionDialog(app, collection));
        popup.add(editItem);
        
        JMenuItem deleteItem = new JMenuItem("Delete Collection");
        deleteItem.addActionListener(ev -> {
            int result = JOptionPane.showConfirmDialog(app, "Are you sure you want to delete the collection '" + collection.name + "'?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                collections.remove(collection.name);
                saveCollections();
                app.refreshDisplay();
            }
        });
        popup.add(deleteItem);
        
        popup.show(source, e.getX(), e.getY());
    }
